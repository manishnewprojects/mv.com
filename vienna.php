 <b><i><u>TL;DR</u>: Historic and functional city. Doesnt feel as opulent as other European royal capitals. Interesting palaces, churches and vibrant public squares  </i></b>
                  
 <br>
 <div class="sharethis-inline-share-buttons"></div>
 <br>

Vienna was the capital of the Austro-Hungarian Empire and has had a long history as the cultural and political center of Europe. It's no surprise to find many historical and grand buildings in the city. Buildings that line impressive straight line boulevards filled focused people going about their jobs.

<img class="mv_image" src="images/vienna/vienna_streetcar-copyright-lanterntravels.jpg" alt="Vienna"/>


My first impression of Vienna was that it almost feels like Germany. I had to remind myself a couple of times that I was actually in Austria.

<img class="mv_image" src="images/vienna/vienna_impressive_buildings-copyright-lanterntravels.jpg" alt="Vienna"/>


The royal buildings I saw felt more working and functional rather than the tributes to rich lifestyles that we see in France, for instance. One of the highlights of Vienna is the Schönbrunn Palace. It set on a huge garden and welcomes visitors with a beautiful facade. You can take a tram ride to get to the palace from wherever you're staying in Vienna. Keep in mind that from center of town it can take some time to reach the castle (40+ minutes), so make sure you plan accordingly. When you get off at the station, just follow the crowds to the entrance.

<img class="mv_image" src="images/vienna/schonbrunn_castle_vienna-copyright-lanterntravels.jpg" alt="Vienna schonbrunn castle"/>


All the markings are very clean and point you towards kiosks where you can get a ticket to tour the palace. Just lie some other places in Europe, the tour setup is unnecessarily complex with too many different packages to choose from. I would recommend you choose the one with the audio tour. I would say it's a really good enhancement to the walking tour.  As you make your way through the palace, you will be get an audio narration of what it was like to be living in the palace, along with details on furnishings and artifacts in the palace. As I've written above, this seems more like a functional palace - almost like touring the White House where there is a functional part to the building and residences. Once you have walked around inside the palace, take a stroll up the hill behind the palace. The winding path goes through beautiful trees and gardens. And from the top of the hill you will see a breathtaking view of Vienna.

<img class="mv_image" src="images/vienna/schonbrunn_grounds_vienna-copyright-lanterntravels.png" alt="Schonbrunn castle"/>

There are also small buses that will ferry you up the hill if you cant make the climb. The beautiful green hillside is also used as a picnic area by the locals. You'll see folks out there with their picnic baskets enjoying a beautiful a meal or kids from a school trips just romping around. It's fun place to just spend a relaxing afternoon. I would say including the travel time back and forth from Schönbrunn Palace it makes for a nice Half day or even a day trip. 



<img class="mv_image" src="images/vienna/schonbrunn_castle_hillside-copyright-lanterntravels.jpg" alt="Vienna palace"/>

I was hoping to see some concerts in Vienna. However the prices are exorbitant and did not feel worthwhile. Some smaller free concerts in local churches are advertised but I was not able to find anything interesting. One interesting building I did see was the headquarters of OPEC (the oil cartel). It was interesting to see the workplace of an organization that has had so much impact on our lives and the world economy over the past 7 decades
 

<img class="mv_image" src="images/vienna/opec_building_vienna-copyright-lanterntravels.jpg" alt="Vienna OPEC building"/>


 
The most vibrant public square in Vienna is the St. Stephen's square. Centerpiece is the tall gothic structure of the St. Stephen's cathedral. This church suffered a lot of damage during the second world war, but has been painstakingly restored. 
<img class="mv_image" src="images/vienna/st_stephens_vienna_nave-copyright-lanterntravels.jpg" alt="Vienna OPEC building"/>


The square is surrounded by many shops and eating places. Beware of pickpockets at this location. We had an iPhone plucked from a purse from someone in our group in under 30 seconds. 

<img class="mv_image" src="images/vienna/st_stephens_market_vienna-copyright-lanterntravels.jpg" alt="Vienna St. Stephens church"/>

If you have some free time you might want to drop into the Naschmarkt, labeled as the biggest open air market in Europe. Honestly, I wasn't very impressed. Seems to be shops run by a lot of a Russians living in Vienna. They came across as very aggressive sales people. You can get some good cheap food in the market, but other than that, there wasn't really much a uniqueness in terms of the things being offered while, you get constantly badgered by salespeople. So if you want to get a feel for an outdoor market in Europe, stop by, or you can give it a skip. 

<img class="mv_image" src="images/vienna/st_stephens_vienna_facade-copyright-lanterntravels.jpg" alt="Vienna St. Stephens church"/>

Vienna is an interesting, historic city that provides a glimpse into the rich political and cultural history of the heard of Europe



  

