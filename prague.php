                 <b><i><u>TL;DR</u>:  Beautiful, historic and friendly city. Very affordable. Take public transportation everywhere. Very walkable too. Get to the Charles bridge early in the morning, followed by a visit to the Castle. Must see Museum of Communism</i></b>
                  
                  <br>
 <div class="sharethis-inline-share-buttons"></div>
 <br>

Prague is one of the most beautiful and very tourist friendly cities I have been to. The whole city is very walkable and unlike some other European cities, will not bite into your wallet. The Czech republic is a part of the European Union, but they did not adopt the Euro as their currency. The local currency is the Czech Koruna and when I visited in April 2018, $1 was worth about 20 Koruna. Prague is a curious concoction of old world European charm and a communist style architectures, so you will regularly find cobbled streets and red roofed buildings right next to communist style building blocks

<img class="mv_image" src="images/prague/prague_castle_charles_bridge_vltava_river.jpg" alt="Prague charles bridge"/>


 You can start exploring the city by going to the Charles Bridge, a very centrally located historic bridge on the Vlatava (Vol-ta-va) river. The bridge itself is a quite fascinating and historic. I would advise you to get there early in the morning if possible before all the tourists arrive, so you can enjoy the beauty of a structure itself along with the views of the river and the buildings along the river. We stayed at an AirBnb that was within walking distance, so getting there early was easy.  
<img class="mv_image" src="images/prague/prague-charles-rives-statues.jpg" alt="Prague charles bridge statues"/>

On the bridge itself, you will find many interesting statues and plaques. Each of those have a somewhat of an interesting history. One talks about an interesting tale of a monk who was thrown over the bridge into the river and eventually came back as a martyr. 
 One of my favorites is of the knight with a dog. I think societies that treat their animals well generally reflect a good human nature. 
<img class="mv_image" src="images/prague/prague-charles-bridge-plaque.jpg" alt="Prague charles bridge plaques"/>

 I was not really impressed with the food choices by the bridge. The area around the bridge seemed kind of touristy and expensive. If a restaurant has a big, bold sign saying "authentic food", that is actually a big warning sign for me.  Pick up a Bagel or two from one of the local bakeries and that should be a good start for the day. A short walk will lead you to the fascinating old Jewish section of town. Here you will find a lot of interesting sites, including a Jewish cemetery or a historic synagogue. 
<img class="mv_image" src="images/prague/prague-jewish-synagogue.jpg" alt="Prague prague-jewish-synagogue"/>

In a testament to the resilience of people, we can see that inspite of the Nazi atrocities, the Jewish population in Prague not only to survived but is now thriving. 

There is a clock on the top of a building with Hebrew letters. I thought that was kind of interesting. 
<img class="mv_image" src="images/prague/prague-jewish-section-hebrew-clock.jpg" alt="Prague prague-jewish-section-hebrew-clock"/>

As you're walking away from the bridge, look up to the hill on your left. You will see an interesting structure which looks like a gigantic metronome. At one time this location had a gigantic monument to Stalin and the Soviet Union by the Czech people. Eventually it was blown up and replaced with the current display. The Stalin monument has a very interesting history, which you can see in detail at the Museum of Communism. Dont miss this musuem.

While in Prague you can also see a famous Frank Gheary building, which was designed to  be office building. It has come to be known as the dancing building or the Fred and Ginger House. That's a pretty interesting building that reflects Gheary's unique architectural vision 

<img class="mv_image" src="images/prague/prague-fred-ginger-frank-gheary-building.jpg" alt="Prague prague-fred-ginger-frank-gheary-building"/>


If you're in Prague around Easter, like I was, you will find yourself in a position to view some of the celebrations. We got to see Polish parishoners  take their Easter baskets to the church for a blessing. Very interesting custom and brought a lot of festive spirit to the old town section

<img class="mv_image" src="images/prague/prague-polish-chruch-St. Giles' Church-wm-mrdreamerskies.jpg" alt="Prague prague-easter"/>

Prague is a very walkable town and also boasts a really fantastic metro system. A system built by the Russians for the Czech people. And it has evolved to a point where it's the most convenient way to get around the city. Also the companion trolley system is wondnerful and you can take it to reach almost any destination in town - like the historic Prague Castle

<img class="mv_image" src="images/prague/prague-castle.jpg" alt="Prague prague-prague-castle"/>


There are many interesting buildings inside the Prague Castle, including the famous Saint Vitus Cathedral. You can walk around the castle area and visit the little market that they have on the ground. 
<img class="mv_image" src="images/prague/prague-castle-st-vitus-cathedral.jpg" alt="Prague prague-castle-st-vitus-cathedral"/>


Also try the traditional dish called tradl, sort of like a stack of donuts stuck together. It's very tasty. It was interesting that folks running the tradle store would not let me film how the sweet pastries are made. I guess they are afraid of Dunkin donuts opening 50 tredl shops across the city ...who knows.
Inside the castle dont miss visiting the large banquet hall. Almost feel like you are there with large crowds celebrating an annual ball or some other event.    

<img class="mv_image" src="images/prague/prague-castle-main-hall.jpg" alt="Prague prague-castle-main-hall"/>

Adjacent to the banquet hall is a balcony from where you can see one of the best views of Prague with its iconic red roofs and church spires. Prague was spared destruction during the second world war, so it has retained a lot of beautiful old buildings.
<img class="mv_image" src="images/prague/prague-view-from-the-castle.jpg" alt="Prague view"/>

 Make sure you take the narrow alley streets and stairs back down to town from the castle. The streets down are lined with very interesting shops and restaurants that you can stop by. While surrounded by all the free spirit, its interesting to note that the town was stanunchly communist in the not too distant past. I found it interesting to think about how people live their lives during that era. A slice of this history can be found at the communist museum, which I really loved visiting

<img class="mv_image" src="images/prague/prague-communism-museum.jpg" alt="Prague prague-communism-museum"/>


One of the interesting parts of town you must visit is the Wenceslas Square. This is a large gathering place and a central part of town where to check people, go to hang out, eat at restaurants, go shopping, and generally express their collective political views. 

<img class="mv_image" src="images/prague/ prague-Wenceslas Square.jpg" alt="Prague prague-Wenceslas"/>


It's a great place for people watching. We also got to see a political demonstration along Wenceslas Square nearby to the square. This area also has some nice shopping areas around where you could buy good, local goods like Bohemian glassware

<img class="mv_image" src="images/prague/wenceslas-demonstration.jpg" alt="Prague wenceslas-demonstration"/>

 
Also, if you get a chance, do go by the river at night and walk along the edge to take a look at the castle. Its a lovely sight.

  
Historic, beautiful, affordable, walkable and a unique cultural treasure - Prague is a fantastic place to visit.


