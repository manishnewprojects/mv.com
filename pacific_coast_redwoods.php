A nice day trip for a walk through history amongst majestic Redwoods and awesome ocean views. From the San Jose or San Francisco area, head south on highway 17 towards Santa Cruz. Start with a stop at the <a target="_blank" href="https://www.parks.ca.gov/?page_id=546"> Henry Cowell Redwoods State Park </a>, which is right off of highway 17, about 20 miles south of the San Jose area. Its is a beautiful park with a very easily accessible trail that will give a wonderful overview of redwoods. 


<img class="mv_image" src="images/pacific_coast_redwoods/henry_cowell_park_trail.jpg" alt="Henry Cowell State Park"/>

You can drive up to the park entrance, pay the required fees, which I think are $10. The parking lot leads to a short one miles trail, which you can finish leisurely in about an hour. 
The trail has been laid out very nicely with number of stops along the way marked with number signs. A booklet you can pickup at the start of the trail provides insights into what you are looking at, like the local ecology, the growth of the redwoods themselves and history of the forest. One of my favorite things, walking on this trail is being able to hear the sounds of the train going by in the woods and the sounds of the people at the station. You're almost transported back in time to when this area was buzzing with train traffic coming from San Francisco.

     
<img class="mv_image" src="images/pacific_coast_redwoods/henry_cowell_park_railroad.jpg" alt="Henry Cowell Redwoods State Park"/>

