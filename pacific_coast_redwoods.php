Its a great area to see and understand the local ecology. I sometimes just love to look at the Clover leaves that grow all along the floor of the gigantic Redwoods. Its a nice reminder of the delicate balance of nature that has the mightiest living things peacefully co-exiust with the smallest of creations. Harmony in nature giveas us a lot to learn from. 

<img class="mv_image" src="images/pacific_coast_redwoods/henry_cowell_park_clover_leaves.jpg" alt="Henry Cowell Park nature"/>

After you finish the trail, you can stop at the ranger Station, look at the exhibits and talk to the staff who will be happy to give you more information about the region. In fact, here is where Teddy Rooselvelt saw the wonders of nature that led him to drive  <a target="_blank" href="https://www.nps.gov/thro/learn/historyculture/theodore-roosevelt-and-conservation.htm"> landmark legislation </a> which has protected these resources for generations to come 


<img class="mv_image" src="images/pacific_coast_redwoods/henry_cowell_roosevelt.jpg" alt="Henry Cowell Roosevelt"/>


From here, you can continue further down towards the ocean on highway 17. Make a pit stop in Santa Cruz. If you are ready to grab a bite, that are many interesting options. Lots of local restaurants. My favorite one is a pizza place, Pizza My heart, one of the best pizzas you can eat on the West Coast. While in Santa Cruz, its also fun to stop by a popular surfing spot. 
<img class="mv_image" src="images/pacific_coast_redwoods/santa_cruz_surfer_spot.jpg" alt="Santa Cruz"/>


Get on Pacific Coast highway one traveling north. Just a few miles out of town you will see a left turn for the <a target="_blank" href="https://www.parks.ca.gov/?page_id=549"> Wilder Ranch State Park </a>. The parking fees you paid at the Henry Cowell park still work for the day, so no need to pay again. From the parking lot, there is a beautiful walk with the best views of this remarkable coast line.

<img class="mv_image" src="images/pacific_coast_redwoods/wilder_ranch_shore.jpg" alt="Wilder Ranch State Park"/>

A short drive up from this park will take you to another awesome place, the <a target="_blank" href="https://www.parks.ca.gov/?page_id=523"> AÃ±o Nuevo State Park </a>. This park also offers a great hike along the ocean. But the best sight is that of gigantic Elephan seals basking on the shore at the end of the hike. 

<img class="mv_image" src="images/pacific_coast_redwoods/ano_neuvo_park_elephant_seals.jpg" alt="Ano neuvo park elephant_seals"/>


Continue further up towards San Francisco and stop by the beautiful <a target="_blank" href="https://www.parks.ca.gov/?page_id=533"> Pigeon point lighthouse </a>. Catch gorgeous views of the Pacific and if you're lucky, you might see whales and dolphins in the ocean. 

<img class="mv_image" src="images/pacific_coast_redwoods/pigeon_point_lighthouse.jpg" alt="Wilder Ranch State Park"/>


From here you can continue the journey along highway one. Stop in half Moon Bay for some more seafood and then you can take highway 92 back up to interstate 280, which will take you to your destination back in the San Jose or San Francisco. This is a wonderful day trip to enjoy the majesty of the redwoods and the breathtaking beauty of the Pacific coastline


