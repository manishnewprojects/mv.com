 <b><i><u>TL;DR</u>: Some of the worlds most magnificent natural sites along the Central California coastline. Easy day trip from San Jose or San Francisco area. Skip tourist traps in Santa Cruz, natural wonders are just a close drive away. Take time to appreciate the views and also learn about the history of the area.  
